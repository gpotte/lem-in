#include "libft.h"

int			ft_atoi(const char *s)
{
	int				i;
	int				j;
	int				sign;

	sign = 1;
	j = 0;
	i = 0;
	while (s[i] > 0 && s[i] < 33)
		++i;
	s[i] == '-' ? sign = -1 : 1;
	if (s[i] == '+' || s[i] == '-')
		++i;
	while (s[i] >= '0' && s[i] <= '9')
	{
		j = (j * 10) + (s[i] - 48);
		++i;
	}
	return (sign * j);
}
